const DICTS: Partial<Record<SupportedLang, Dict>> = { English: en, Chinese: zh, Japanese: ja, Korean: ko, Vietnamese: vi, Spanish: es, French: fr, German: de };

export function t(uiLang: string, key: DictKey, ...args: string[]): string {
  const lang = (DICTS[uiLang as SupportedLang] ? (uiLang as SupportedLang) : 'English');
  const dict = DICTS[lang]!;
  const v = dict[key];
  return typeof v === 'function' ? (v as Function)(...(args.length ? args : ['']), lang) : v as string;
}

// Browser → SupportedLang mapping. Used to seed uiLang on first visit.
export function detectUiLang(): SupportedLang {
  if (typeof navigator === 'undefined') return 'English';
  const tag = (navigator.language || '').toLowerCase();
  if (tag.startsWith('zh')) return 'Chinese';
  if (tag.startsWith('ja')) return 'Japanese';
  if (tag.startsWith('ko')) return 'Korean';
  if (tag.startsWith('vi')) return 'Vietnamese';
  if (tag.startsWith('es')) return 'Spanish';
  if (tag.startsWith('fr')) return 'French';
  if (tag.startsWith('de')) return 'German';
  return 'English';
}

// Sensible default L1/L2 pair given the UI language. The user can still
// override either via the form dropdowns.
export function defaultLangPair(uiLang: SupportedLang): { source: SupportedLang; target: SupportedLang } {
  return uiLang === 'English'
    ? { source: 'English', target: 'Chinese' }
    : { source: uiLang, target: 'English' };
}

// Canonical keys for closed-form selects. Stored in state/localStorage instead
// of localized display strings so values stay correct across UI language changes.
export const AGE_KEYS = ['ageChild', 'ageYoung', 'ageTeen', 'ageAdult'] as const;
export type AgeKey = typeof AGE_KEYS[number];

export const DOMAIN_KEYS = ['indGeneral', 'indIT', 'indMedical', 'indBusiness', 'indLegal', 'indEducation', 'indDesign', 'indSales', 'indTravel', 'indLogistics', 'indWorkplace', 'indSchool', 'indHobby', 'indSports', 'indSocial', 'indStories', 'indAnimals', 'indArts', 'indMusic'] as const;
export type DomainKey = typeof DOMAIN_KEYS[number];

